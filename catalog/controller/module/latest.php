	public function index($setting) {
		$this->load->language('module/category');

		$data['heading_title'] = $this->language->get('heading_title');

		if (isset($this->request->get['path'])) {
			$parts = explode('_', (string)$this->request->get['path']);
		} else {
			$parts = array();
		}

		if (isset($parts[0])) {
			$data['category_id'] = $parts[0];
		} else {
			$data['category_id'] = 0;
		}

		if (isset($parts[1])) {
			$data['child_id'] = $parts[1];
		} else {
			$data['child_id'] = 0;
		}

		$this->load->model('catalog/category');

		$this->load->model('catalog/product');

		$data['categories'] = array();

		$categories = $this->model_catalog_category->getLimitCategories($setting['limit'], 0);

		foreach ($categories as $category) {
			$children_data = array();

			if ($category['category_id'] == $data['category_id']) {
				$children = $this->model_catalog_category->getCategories($category['category_id']);

				foreach ($children as $child) {
					$filter_data = array('filter_category_id' => $child['category_id'], 'filter_sub_category' => true);

					$children_data[] = array(
						'category_id' => $child['category_id'],
						'name' => $child['name'] . ($this->config->get('config_product_count') ? ' (' . $this->model_catalog_product->getTotalProducts($filter_data) . ')' : ''),
						'href' => $this->url->link('product/category', 'path=' . $category['category_id'] . '_' . $child['category_id'])
					);
				}
			}

			$filter_data = array(
				'filter_category_id' => $category['category_id'],
				'filter_sub_category' => true
			);

			//$description = "Фотообои, картины, дизайнерские коллекции элитных фотообоев и рельефных панно";

			$data['categories'][] = array(
				'category_id' => $category['category_id'],
				'name' => $category['name'],
				'children' => $children_data,
				'href' => $this->url->link('product/category', 'path=' . $category['category_id']),
				'thumb' => $this->model_tool_image->resize($category['image'], 415, 340),
				'description' => utf8_substr(strip_tags(html_entity_decode($category['description'], ENT_QUOTES, 'UTF-8')), 0, $this->config->get('config_category_description_length')) . '',
			);

		}


		if (file_exists(DIR_TEMPLATE . $this->config->get('config_template') . '/template/module/latest.tpl')) {
			return $this->load->view($this->config->get('config_template') . '/template/module/latest.tpl', $data);
		} else {
			return $this->load->view('default/template/module/latest.tpl', $data);
		}

	}


}
